int	ft_strlen(char *str)
{
	int	i;
	int	x;

	i = 0;
	x = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= 65 || str[i] <= 90 || str[i] >= 97 || str[i] <= 122)
			x++;
		i++;
	}
	return(x);
}
