char	*ft_strcat(char *dest, char *src)
{
	int i;
	int x;
	
	i = 0;
	x = 0;
	while (dest[i] != '\0')
	{
		x++;
		i++;
	}
	i = 0;
	while (src[i] != '\0')
	{
		dest[x + i] = src[i];
		i++;
	}
	dest[x + i] = '\0';
	return (dest);
}
